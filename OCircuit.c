void plot_Cpixel(int x, int y, bool erase, unsigned char color){
    int vramx = 0;  // each 32 register makes up 32 pixels, for 256 pixels there are 8x 32 registers 
    uint32_t bit_pos = 0;   // each 32 register makes up 4 pixels, for 256 pixels there are 64x 32 registers 
    
    
    vramx = scan_Hram(x);
    
    
    
    if(!erase){
      bit_pos |= (color)<<((x%4)*8);      
      C_pixels[y][vramx] |= bit_pos;
    }
    if(erase){
      C_pixels[y][vramx] &= ~((color)<<((x%4)*8));
    }
    
    
}

