void tone(int note){
	pwm_set_enabled(slice_num, false);        // gotta put this into a tone function		
    float div = 125000000 / (note * 10000);
	pwm_config_set_clkdiv(&config, div);
    pwm_init(slice_num, &config, true);
    pwm_set_gpio_level(15, 30000); // 50% duty cycle
}

void tune(int melody[], int noteDurations[]){		// right now tunes will pause the whole processor
	for (int thisNote = 0; thisNote < 8; thisNote++) {
              tone(melody[thisNote]);
              pwm_set_gpio_level(15, 30000); // 50% duty cycle
              int noteDuration = 1000 / noteDurations[thisNote];
              sleep_ms(noteDuration);
              int pauseBetweenNotes = noteDuration * 1.30;
              pwm_set_gpio_level(15, 0);
              sleep_ms(pauseBetweenNotes);
          }
}

